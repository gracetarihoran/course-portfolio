# Variables for rectangle
'''
Declare two variables: length and width.
Assign the value 10 to length and 33 to width.
Calculate the area of the rectangle, rectangle_area,
using the formula: area = length * width.
Print the result in the exact format:
The area of the rectangle is: [result]
'''
length = 10
width = 33

# Variables for circle
'''
Declare two variables: PI and radius.
Assign the value 3.14 to PI and 5 to radius.
Calculate the area of the circle, circle_area,
using the formula: area = PI * (radius ** 2).
Print the result in the exact format:
The area of the circle is: [result]
'''
PI = 3.14
radius = 5

# Calculate and print the rectangle's area
rectangle_area = length * width
print(f"The area of the rectangle is: {rectangle_area}")

# Calculate and print the circle's area
circle_area = PI * (radius ** 2)
print(f"The area of the circle is: {circle_area}")
